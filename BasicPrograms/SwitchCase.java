
public class SwitchCase {
	public static void main(String[] args) {
		int n=1;
		switch(n++) {
		case 1:
			System.out.println(n);
			//break;
		case 3:
			System.out.println(n);
			//break;
		case 5:
			System.out.println(n);
			break;
		case 7:
			System.out.println(n);
			break;
		case 9:
			System.out.println(n);
			break;
		
			
			
		}
		int k =-4;
		System.out.println(k>>1);
		System.out.println(Integer.toBinaryString(k));
		
	}
}
